/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
//	MPU9250_get_data();
	Count++;
	if(Count%100==0){}
	if(Count%5==0)
	{
    get_imu_data();
//		yaw=pose.yaw;
			HcSr04_start_1();
			HcSr04_start_2();
			HcSr04_start_3();
		IR_1_flag=(HAL_GPIO_ReadPin(IR_1_GPIO_Port, IR_1_Pin)==GPIO_PIN_RESET);//探测到为1，探测不到为0
		IR_2_flag=(HAL_GPIO_ReadPin(IR_2_GPIO_Port, IR_2_Pin)==GPIO_PIN_RESET);
//		IR_3_flag=HAL_GPIO_ReadPin(IR_3_GPIO_Port, IR_3_Pin);
//		IR_4_flag=HAL_GPIO_ReadPin(IR_4_GPIO_Port, IR_4_Pin);
//		ANODT_SendF4(dis_1,dis_2,dis_3,0);
}
	if((Count%5==0)&&start_flag)
	{
		Count=0;
		GetEncoder();
		restart();//速度归0，重新赋予速度
		advance(basic_speek);//赋予基础速度，其他运动会在此基础上进行增减
		if(keep_yaw_flag)  keep_yaw();//保持方向
		if(keep_dis_1_flag)  keep_dis_1();//保持距离
		if(keep_dis_2_flag)  keep_dis_2();//保持距离
		if(keep_dis_3_flag)  keep_dis_3();//保持距离	
		if(keep_dis_23_flag) keep_dis_23();
		set_pwm[0]=PID_calc(target_speek[0],Encoder[0],&pid_motor_1);
		set_pwm[1]=PID_calc(target_speek[1],Encoder[1],&pid_motor_2);
		set_pwm[2]=PID_calc(target_speek[2],Encoder[2],&pid_motor_3);
		set_pwm[3]=PID_calc(target_speek[3],Encoder[3],&pid_motor_4);
		if(move_flag) set_motor_pwm(set_pwm);else set_motor_pwm(zero_pwm);//赋予pwm
	}
		if(Count>=1000)Count=0;
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

